//Write a program to check whether a given string starts with the contents of another string

public class Task_6 {

	public static void main(String[] args) {


		String str = "Sleeping World";
		
		System.out.println("Given String Is : "+str);
		
		System.out.println("Given String is starts with hello : "+(str.startsWith("hello")));
		System.out.println("Given String is starts with Sleep : "+(str.startsWith("Sleep")));

	}

}


// Output : 



// Given String Is : Sleeping World
// Given String is starts with hello : false
// Given String is starts with Sleep : true







