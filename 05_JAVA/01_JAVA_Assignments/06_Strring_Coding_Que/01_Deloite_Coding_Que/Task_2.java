//Write a program to get the character at the given index within the String

public class Task_2 {

	public static void main(String[] args) {


		String str = "Helloworld";
		
		System.out.println("Given String Is : "+str);
		
		System.out.println("Character at index 0 is : "+(str.charAt(0)));
		System.out.println("Character at index 9 is : "+(str.charAt(9)));

	}

}


// Output : 


// Given String Is : Helloworld
// Character at index 0 is : H
// Character at index 9 is : d



