
//Write a program to convert all the characters in a string to Uppercase

public class Task_40 {

	public static void main(String[] args) {

		String str = "welcome to sleeping world";
		System.out.println("Given String : " + str);

		System.out.println("Result : " + str.toUpperCase());
	}
}


// Output :

// Given String : welcome to sleeping world
// Result : WELCOME TO SLEEPING WORLD

