//Write a program to check whether a given string ends with the contents of another string

public class Task_3 {

	public static void main(String[] args) {


		String str = "Welcome";
		
		System.out.println("Given String Is : "+str);
		
		System.out.println("Given String ends with come : "+(str.endsWith("come")));
		
		System.out.println("Given String is ends with meme : "+(str.endsWith("meme")));

	}

}


// Output :


// Given String Is : Welcome
// Given String ends with come : true
// Given String is ends with meme : false 





