
//Write a program to get the length of a given string

public class Task_32 {

	public static void main(String[] args) {

		String str = "Welcome to Sleeping World";
		System.out.println("Given String : " + str);

		int lengthOfString = str.length();
		System.out.println("Length Of Given String Is : " + lengthOfString);
	}
}


// Output :

// Given String : Welcome to Sleeping World
// Length Of Given String Is : 25

